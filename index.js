function add(a, b) {
    return a+b;
}
function subtract(a, b) {
    return a-b;
}
function multiply(a, b) {
    return a*b;
}
function divide(a, b) {
    return a/b;
}



function increment(x) {
    return ++x;
}
function decrement(x) {
    return --x;
}
 function makeInt(x) {
    return parseInt(x, 10);
 }
 function preserveDecimal(x) {
    return parseFloat(x);
 }